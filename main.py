# 6.1. –£–ø—Ä–∞–≤–ª–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è–º–∏
async def get_or_create_user(user: types.User):
    """–°–æ–∑–¥–∞–µ—Ç –∏–ª–∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–≥–æ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"""
    session = get_db_session()
    try:
        db_user = session.query(User).filter_by(telegram_id=user.id).first()
    
        if not db_user:
            db_user = User(
                telegram_id=user.id,
                username=user.username,
                first_name=user.first_name,
                last_name=user.last_name
            )
            session.add(db_user)
            session.commit()
            session.refresh(db_user) # –û–±–Ω–æ–≤–ª—è–µ–º –æ–±—ä–µ–∫—Ç –¥–ª—è –ø–æ–ª—É—á–µ–Ω–∏—è ID
            logger.info(f"–°–æ–∑–¥–∞–Ω –Ω–æ–≤—ã–π –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å: {user.id}")
    
        return db_user
    finally:
        session.close()

# 6.2. –°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ –∏ –ø–æ–ª—É—á–µ–Ω–∏–µ –∏—Å—Ç–æ—Ä–∏–∏ –¥–∏–∞–ª–æ–≥–∞
async def save_message(user_id: int, role: str, content: str):
    """–°–æ—Ö—Ä–∞–Ω—è–µ—Ç —Å–æ–æ–±—â–µ–Ω–∏–µ –≤ –ë–î"""
    session = get_db_session()
    try:
        message = Conversation(
            user_id=user_id,
            role=role,
            content=content
        )
        session.add(message)
        session.commit()
    finally:
        session.close()

async def get_chat_history(user_id: int) -> list:
    """–í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∏—Å—Ç–æ—Ä–∏—é –¥–∏–∞–ª–æ–≥–∞ –∏–∑ –ë–î"""
    session = get_db_session()
    try:
        messages = session.query(Conversation).filter_by(user_id=user_id).order_by(Conversation.timestamp.asc()).all()
    
        # –§–æ—Ä–º–∞—Ç–∏—Ä—É–µ–º –∏—Å—Ç–æ—Ä–∏—é –¥–ª—è API
        history = [{"role": msg.role, "content": msg.content} for msg in messages]
    
        # –û–≥—Ä–∞–Ω–∏—á–∏–≤–∞–µ–º —Ä–∞–∑–º–µ—Ä –∏—Å—Ç–æ—Ä–∏–∏
        if len(history) > MAX_HISTORY * 2 + 1:
            return [history[0]] + history[-(MAX_HISTORY * 2):]
        return history
    finally:
        session.close()

# 6.3. –ü–æ—Ç–æ–∫–æ–≤–∞—è –ø–µ—Ä–µ–¥–∞—á–∞ –æ—Ç–≤–µ—Ç–∞ –æ—Ç DeepSeek API
async def stream_deepseek_response(user_id: int, history: list, message: types.Message):
    """–ü–æ—Ç–æ–∫–æ–≤–∞—è –ø–µ—Ä–µ–¥–∞—á–∞ –æ—Ç–≤–µ—Ç–∞ –æ—Ç DeepSeek API"""
    url = "https://api.deepseek.com/v1/chat/completions"
    headers = {
        "Authorization": f"Bearer {os.getenv('DEEPSEEK_API_KEY')}",
        "Content-Type": "application/json"
    }
    payload = {
        "model": MODEL_NAME,
        "messages": history,
        "temperature": 0.7,
        "max_tokens": 4096,
        "stream": True
    }
    
    full_response = ""
    last_update = datetime.now()
    message_sent = False
    
    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º –∑–∞–ø—Ä–æ—Å –∫ DeepSeek API
    async with aiohttp.ClientSession() as session:
        async with session.post(url, json=payload, headers=headers) as response:
            if response.status != 200:
                error = await response.text()
                logger.error(f"DeepSeek API error: {response.status} - {error}")
                await message.answer("‚ö†Ô∏è –û—à–∏–±–∫–∞ API. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")
                return
            
            # –ü–∞—Ä—Å–∏–º –æ—Ç–≤–µ—Ç –æ—Ç DeepSeek API
            async for chunk in response.content.iter_any():
                if chunk:
                    try:
                        chunk_str = chunk.decode('utf-8')
                        if chunk_str.startswith("data: "):
                            json_str = chunk_str[6:].strip()
                            if json_str == "[DONE]":
                                break
                            
                            # –ü–∞—Ä—Å–∏–º JSON
                            data = json.loads(json_str)
                            
                            if "choices" in data and len(data["choices"]) > 0:
                                delta = data["choices"][0].get("delta", {})
                                if "content" in delta:
                                    token = delta["content"]
                                    full_response += token
                                    
                                    # –û—Ç–ø—Ä–∞–≤–ª—è–µ–º —á–∞—Å—Ç–∏—á–Ω—ã–µ –æ–±–Ω–æ–≤–ª–µ–Ω–∏—è –∫–∞–∂–¥—ã–µ 0.5 —Å–µ–∫
                                    if not message_sent:
                                        msg = await message.answer(full_response)
                                        message_sent = True
                                    elif (datetime.now() - last_update).total_seconds() > 0.5:
                                        # –û–±–Ω–æ–≤–ª—è–µ–º —Å—É—â–µ—Å—Ç–≤—É—é—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ
                                        await bot.edit_message_text(
                                            chat_id=message.chat.id,
                                            message_id=msg.message_id,
                                            text=full_response
                                        )
                                        last_update = datetime.now()
                    except json.JSONDecodeError:
                        logger.error(f"–û—à–∏–±–∫–∞ –¥–µ–∫–æ–¥–∏—Ä–æ–≤–∞–Ω–∏—è JSON: {json_str}")
                    except Exception as e:
                        logger.error(f"–û—à–∏–±–∫–∞ –æ–±—Ä–∞–±–æ—Ç–∫–∏ –ø–æ—Ç–æ–∫–∞: {e}")
    
    # –§–∏–Ω–∞–ª–∏–∑–∏—Ä—É–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ
    if not message_sent:
        await message.answer(full_response)
    else:
        await bot.edit_message_text(
            chat_id=message.chat.id,
            message_id=msg.message_id,
            text=full_response
        )
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–æ–ª–Ω—ã–π –æ—Ç–≤–µ—Ç –≤ –ë–î
    await save_message(user_id, "assistant", full_response)

